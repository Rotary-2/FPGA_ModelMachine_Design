`include "define.v"

module InstMem(
    input wire ce,
    input wire [31:0] addr,
    output wire [31:0] data
);

reg [31:0] instmem [1023:0];

assign data = (ce)? instmem[addr[11:2]] : 0;

initial begin

    // -----------------------------
    // 基础数据准备
    // -----------------------------
    instmem[0]  = 32'h2001000A; // addi $1,$0,10
    instmem[1]  = 32'h20020003; // addi $2,$0,3

    // -----------------------------
    // add / sub
    // -----------------------------
    instmem[2]  = 32'h00221820; // add  $3,$1,$2  ->13
    instmem[3]  = 32'h00222022; // sub  $4,$1,$2  ->7

    // -----------------------------
    // and / or / xor
    // -----------------------------
    instmem[4]  = 32'h00222824; // and  $5,$1,$2
    instmem[5]  = 32'h00223025; // or   $6,$1,$2
    instmem[6]  = 32'h00223826; // xor  $7,$1,$2

    // -----------------------------
    // sll / srl
    // -----------------------------
    instmem[7]  = 32'h00024080; // sll  $8,$2,2
    instmem[8]  = 32'h00024882; // srl  $9,$2,2

    // -----------------------------
    // slt
    // -----------------------------
    instmem[9]  = 32'h0022502A; // slt  $10,$1,$2

    // -----------------------------
    // mult / mfhi / mflo
    // -----------------------------
    instmem[10] = 32'h00220018; // mult $1,$2
    instmem[11] = 32'h00005812; // mflo $11
    instmem[12] = 32'h00006010; // mfhi $12

    // -----------------------------
    // div / mfhi / mflo
    // -----------------------------
    instmem[13] = 32'h0022001A; // div $1,$2
    instmem[14] = 32'h00006812; // mflo $13
    instmem[15] = 32'h00007010; // mfhi $14

    // -----------------------------
    // sw / lw
    // -----------------------------
    instmem[16] = 32'hAC030000; // sw $3,0($0)
    instmem[17] = 32'h8C0F0000; // lw $15,0($0)

    // -----------------------------
    // mtc0 / mfc0 测试
    // -----------------------------
    instmem[18] = 32'h40816000; // mtc0 $1,$12
    instmem[19] = 32'h40016800; // mfc0 $1,$13

    // -----------------------------
    // eret 测试
    // -----------------------------
    instmem[20] = 32'h42000018; // eret

end

endmodule
